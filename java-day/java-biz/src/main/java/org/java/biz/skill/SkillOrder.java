package org.java.biz.skill;

public class SkillOrder {
	
	
	private int userId;
	
	private int skOrdStatus;
	
	private  int productId;

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getSkOrdStatus() {
		return skOrdStatus;
	}

	public void setSkOrdStatus(int skOrdStatus) {
		this.skOrdStatus = skOrdStatus;
	}

	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}
	
	
	
	

}
